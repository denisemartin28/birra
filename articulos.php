        <h3>BLONDE ALE</h3>
      </article>
        <span>cerveza dorada de fermentación alta, perteneciente a la familia de las ALE, de moderada intensidad que tiene una complejidad sutil frutal-especiada a levadura belga, un poco de sabor a malta dulce y un final seco.
        	CARACTERISTICAS:
        Alcohol – 3.8%.
        Amargor – 28 IBUs.</span>

      <article class="">
        <h3>OKCTOBERFEST</h3>
      </article>
        <span>El sabor a malta inicial a menudo sugiere dulzura, pero de acabado moderadamente seco. El complejo y único carácter maltoso a menudo incluye notas a pan y tostado. El amargor procedente del lúpulo es moderado, a pesar de que su sabor sea de bajo a imperceptible. Los lúpulos, sin embargo, otorgan a la cerveza el equilibrio suficiente para que no predomine el dulzor de la malta.
        	CARACTERISTICAS:
        Alcohol – 6.2%.
        Amargor –  30 IBUs.</span>

      <article class="">
        <h3>IPA</h3>
      </article>
        <span>Cerveza de fermentación alta y calificada como pálida, para diferenciarla de las características sobre las Brown, Stout o Porters. Presentan un color pálido anaranjado similar al ámbar de menor o mayor intensidad y poseen un mayor aporte de lúpulo que proporciona un mayor grado de amargor, aparte de una serie de matices aromáticos y de sabor particular.
        	CARACTERISTICAS:
        Alcohol – 5.1%.
        Amargor –  40 IBUs.</span>
        
      <article class="">
        <h3>SESION IPA</h3>
      </article>
        <span>Cerveza fácil de beber pero sin dejar de lado las características de la IPA, tiene un sabor algo más seco que las Pale Ales, aunque es innegable la gran similitud entre ambas.  Posee un cuerpo sumamente ligero y junto al lúpulo viene el intenso aroma, pero sin el amargor pronunciado ni el alto porcentaje de alcohol.
        	CARACTERISTICAS:
        Alcohol – 4.5%.
        Amargor – 50 IBUs.</span>
